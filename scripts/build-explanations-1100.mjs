function norm(s) {
  return String(s || "").toLowerCase().replace(/ё/g, "е");
}

function findPlant(text) {
  const t = norm(text);
  for (const [key, info] of Object.entries(PLANTS)) {
    if (t.includes(key)) return { key, ...info };
  }
  return null;
}

function findPrep(text) {
  const t = norm(text);
  for (const [key, info] of Object.entries(PREPS)) {
    if (t.includes(key)) return { key, info };
  }
  return null;
}

function ans(q) {
  return q.correct.map((i) => q.options[i]).filter(Boolean);
}

function ansText(q) {
  return ans(q).join("; ");
}

