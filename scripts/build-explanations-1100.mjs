    text === "Молекула — электронейтральная частица, состоящая из атомов и сохраняющая химические свойства вещества." ||
    text.startsWith("Правильный ответ:") ||
    text.includes("выбирайте вариант, который точно соответствует") ||
    text.includes("Ориентируйтесь на ключевые слова") ||
    (text.startsWith("Ответ:") && text.includes("соответствующее формулировке вопроса"))
